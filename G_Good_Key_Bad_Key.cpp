void solve() 
{
    int n,k;
    cin>>n>>k;
    int a[n];
    for (int i = 0; i < n; i++)
    {
        cin>>a[i];
    }
    int ans=0,sum=0;
    for (int i = 0; i <= n; i++)
    {
        int curr=sum;
        int z=1;
        for (int j = i; j < min(n,i+32); j++)
        {
            curr+=a[j]>>z;
            z++;
        }
        ans=max(ans,curr);
        if(i!=n){
            sum+=a[i]-k;
        }
    }
    cout<<ans<<endl;
}

