void solve()
{
    int n;
    cin>>n;
    int a[n];
    for (int i = 0; i < n; i++)
    {
        cin>>a[i];
    }
    bool f=false;
    int ans=0;
    int Max=1;
    for (int i = 0; i < n; i++)
    {
        if(f&&a[i]<=Max){
            ans++;
            Max=max(a[i]+1,Max);
        }
        else if(a[i]==1){
            f=true;
            ans++;
            Max=max(a[i]+1,Max);
        }
    }
    cout<<ans<<endl;
}

