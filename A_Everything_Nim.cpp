void solve()
{
    int n;
    cin>>n;
    int a[n];
    for (int i = 0; i < n; i++)
    {
        cin>>a[i];
    }
    sort(a,a+n);
    int x=1;
    
    for (int i = 0; i < n; i++)
    {
        if(a[i]>x){
            break;
        }
        if(a[i]==x){
            x++;
        }
    }
    if((x%2==0&&a[n-1]!=x-1)||(x%2==1&&a[n-1]==x-1)){
        cout<<"Bob\n";
    }
    else{
        cout<<"Alice\n";
    }
}

