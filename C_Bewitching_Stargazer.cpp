void solve() 
{
    int n,k;cin>>n>>k;
    int m=n+1,sum=0,cur=1;
    while(n>=k){
        if(n&1)sum+=cur;
        n>>=1;
        cur<<=1;
    }
    cout<<m*sum/2<<endl;
}

